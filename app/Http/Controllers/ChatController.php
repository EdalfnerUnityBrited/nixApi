    */
    public function newchat(Request $request) {
        $data = json_decode($request->getContent(), true);
        
        Validator::make($data, [
            'id_usuario'     => 'required',
            'id_proveedor'    => 'required',
        ])->validate();

        $chat = new Chat($data);
        $chat->save();
        return response()->json([
                    'message' => 'Successfully created chat!'], 201);
    }

